const zkAppPrivateKey = PrivateKey.random();
const zkAppAddress = zkAppPrivateKey.toPublicKey();
const zkAppInstance = new Canvas(zkAppAddress);
const canvasData = new CanvasData(
  raw.map((row) => row.map((col) => Boolean(col)))
);

let tx = await Mina.transaction(account, () => {
  Party.fundNewAccount(account);

  zkAppInstance.deploy({ zkappKey: zkAppPrivateKey });

  zkAppInstance.init(canvasData);
});
await tx.send().wait();

console.log('Deployed!');

zkAppInstance.assertValidCanvas(canvasData);

canvasData.switch(10, 10);
canvasData.switch(10, 11);
canvasData.switch(11, 10);

tx = await Mina.transaction(account, () => {
  zkAppInstance.update(canvasData);
});
await tx.send().wait();

console.log('Updated!');

zkAppInstance.assertValidCanvas(canvasData);

await shutdown();
